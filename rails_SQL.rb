SQL (Structured Query Language)

-SQL lets you access and manipulate databases

Some of The Most Important SQL Commands

SELECT - extracts data from a database
UPDATE - updates data in a database
DELETE - deletes data from a database
INSERT INTO - inserts new data into a database
CREATE DATABASE - creates a new database
ALTER DATABASE - modifies a database
CREATE TABLE - creates a new table
ALTER TABLE - modifies a table
DROP TABLE - deletes a table
CREATE INDEX - creates an index (search key)
DROP INDEX - deletes an index


SELECT * FROM Customers; => displays the whole table
SELECT CustomerName,postal FROM Customers; => only shows contact name
& postal
SELECT DISTINCT Country FROM Customers; => does not show duplicates
SELECT COUNT(DISTINCT Country) FROM Customers => shows count w/out duplicates



